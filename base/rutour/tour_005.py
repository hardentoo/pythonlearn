line1 = "Это строка"
line2 = "ЭТО ВТОРАЯ СТРОКА"
print(line1.replace('то', 'хо'))  # Замена одной подстроки другой
print(line1.find('то'))  # Поиск смещения подстроки
print(line1.split(' '))  # Разбивает строку по разделителю
print(line1.upper())  # Преобразование символов в верхний регистр
print(line2.lower())  # Преобразование символов в нижний регистр
